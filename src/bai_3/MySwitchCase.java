package bai_3;

public class MySwitchCase {

	public static void main(String[] args) {
		
		int a = 1;
		
		switch(a){
			case 1:
				System.out.println("case 1");
				break;
			case 2:
				System.out.println("case 2");
				break;
			case 3:
				System.out.println("case 3");
				break;
			default:
				System.out.println("default case");
				break;
		}

	}

}
