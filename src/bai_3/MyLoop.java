package bai_3;

public class MyLoop {

	public static void main(String[] args) {
		//for loop
		for(int i = 0; i < 10; i++){
			System.out.println(i);
		}
		
		int i = 1;
		//while loop
		while(i < 5){
			System.out.println(i);
			i++;
		}
		
		//do while loop
		i = 1;
		do {
			System.out.println(i);
		} while (i < 5);

	}

}
